-module(ipto_graphql_schema).

-export([schema/0]).

-spec schema() -> binary().
schema() ->
    <<
        "schema {\n"
        "  query: Query\n"
        "  mutation: Mutation\n"
        "}\n\n"
        "type Query {\n"
        "  unit(tenantid: Int!, unitid: Int!): Unit\n"
        "  unitByCorrid(corrid: String!, tenantid: Int): Unit\n"
        "  unitExists(tenantid: Int!, unitid: Int!): Boolean!\n"
        "  unitLocked(tenantid: Int!, unitid: Int!): Boolean!\n"
        "  units(query: String, orderField: String, orderDir: String, limit: Int, offset: Int): SearchResult!\n"
        "  unitsByExpression(tenantid: Int, unitid: Int, status: Int, name: String, nameLike: String, corrid: String, orderField: String, orderDir: String, limit: Int, offset: Int): SearchResult!\n"
        "  rightRelation(tenantid: Int!, unitid: Int!, reltype: Int!): Relation\n"
        "  rightRelations(tenantid: Int!, unitid: Int!, reltype: Int!): [Relation!]!\n"
        "  leftRelations(tenantid: Int!, unitid: Int!, reltype: Int!): [Relation!]!\n"
        "  countRightRelations(tenantid: Int!, unitid: Int!, reltype: Int!): Int!\n"
        "  countLeftRelations(tenantid: Int!, unitid: Int!, reltype: Int!): Int!\n"
        "  rightAssociation(tenantid: Int!, unitid: Int!, assoctype: Int!): Association\n"
        "  rightAssociations(tenantid: Int!, unitid: Int!, assoctype: Int!): [Association!]!\n"
        "  leftAssociations(assoctype: Int!, assocstring: String!): [Association!]!\n"
        "  countRightAssociations(tenantid: Int!, unitid: Int!, assoctype: Int!): Int!\n"
        "  countLeftAssociations(assoctype: Int!, assocstring: String!): Int!\n"
        "  tenantInfo(id: Int, name: String): TenantInfo\n"
        "  attributeInfo(id: Int, name: String): AttributeInfo\n"
        "  inspectGraphqlSdl(sdl: String!): SdlCatalog!\n"
        "  registeredOperations: RegisteredOperations!\n"
        "}\n\n"
        "type Mutation {\n"
        "  createUnit(tenantid: Int!, name: String): Unit!\n"
        "  inactivateUnit(tenantid: Int!, unitid: Int!): Boolean!\n"
        "  activateUnit(tenantid: Int!, unitid: Int!): Boolean!\n"
        "  requestStatusTransition(tenantid: Int!, unitid: Int!, status: Int!): StatusTransitionResult!\n"
        "  transitionUnitStatus(tenantid: Int!, unitid: Int!, status: Int!): StatusTransitionResult!\n"
        "  lockUnit(tenantid: Int!, unitid: Int!, locktype: Int!, purpose: String!): Boolean!\n"
        "  unlockUnit(tenantid: Int!, unitid: Int!): Boolean!\n"
        "  addRelation(tenantid: Int!, unitid: Int!, reltype: Int!, reltenantid: Int!, relunitid: Int!): Boolean!\n"
        "  removeRelation(tenantid: Int!, unitid: Int!, reltype: Int!, reltenantid: Int!, relunitid: Int!): Boolean!\n"
        "  addAssociation(tenantid: Int!, unitid: Int!, assoctype: Int!, assocstring: String!): Boolean!\n"
        "  removeAssociation(tenantid: Int!, unitid: Int!, assoctype: Int!, assocstring: String!): Boolean!\n"
        "  configureGraphqlSdl(sdl: String!): SdlConfigureResult!\n"
        "  configureGraphqlSdlFile(path: String!): SdlConfigureResult!\n"
        "}\n\n"
        "type SearchResult {\n"
        "  total: Int!\n"
        "  results: [Unit!]!\n"
        "}\n\n"
        "type Unit {\n"
        "  tenantid: Int!\n"
        "  unitid: Int!\n"
        "  unitver: Int!\n"
        "  status: Int!\n"
        "  unitname: String\n"
        "  corrid: String\n"
        "}\n\n"
        "type Relation {\n"
        "  tenantid: Int!\n"
        "  unitid: Int!\n"
        "  reltype: Int!\n"
        "  reltenantid: Int!\n"
        "  relunitid: Int!\n"
        "}\n\n"
        "type Association {\n"
        "  tenantid: Int!\n"
        "  unitid: Int!\n"
        "  assoctype: Int!\n"
        "  assocstring: String!\n"
        "}\n\n"
        "type StatusTransitionResult {\n"
        "  status: Int!\n"
        "}\n\n"
        "type RegisteredOperations {\n"
        "  queries: [RegisteredOperationName]!\n"
        "  mutations: [RegisteredOperationName]!\n"
        "}\n\n"
        "type RegisteredOperationName {\n"
        "  name: String!\n"
        "}\n\n"
        "type TenantInfo {\n"
        "  id: Int!\n"
        "  name: String\n"
        "  description: String\n"
        "  created: String\n"
        "}\n\n"
        "type AttributeInfo {\n"
        "  id: Int!\n"
        "  alias: String\n"
        "  name: String\n"
        "  qualname: String\n"
        "  type: Int\n"
        "  forced_scalar: Boolean\n"
        "}\n\n"
        "type SdlCatalog {\n"
        "  attribute_registry: SdlAttributeRegistry!\n"
        "  records: [SdlRecordShape!]!\n"
        "  templates: [SdlTemplateShape!]!\n"
        "  counts: SdlCounts!\n"
        "  errors: [SdlError!]!\n"
        "}\n\n"
        "type SdlAttributeRegistry {\n"
        "  found: Boolean!\n"
        "  enum_name: String\n"
        "  attributes: [String!]!\n"
        "  attribute_defs: [SdlAttributeDef!]!\n"
        "}\n\n"
        "type SdlAttributeDef {\n"
        "  symbol: String!\n"
        "  datatype: String\n"
        "  is_array: Boolean!\n"
        "  name: String\n"
        "  qualname: String\n"
        "  uri: String\n"
        "  description: String\n"
        "}\n\n"
        "type SdlRecordShape {\n"
        "  type_name: String!\n"
        "  attribute: String\n"
        "  uses: [SdlFieldUse!]!\n"
        "}\n\n"
        "type SdlTemplateShape {\n"
        "  type_name: String!\n"
        "  name: String\n"
        "  uses: [SdlFieldUse!]!\n"
        "}\n\n"
        "type SdlFieldUse {\n"
        "  field_name: String!\n"
        "  attribute: String!\n"
        "}\n\n"
        "type SdlCounts {\n"
        "  attributes: Int!\n"
        "  records: Int!\n"
        "  templates: Int!\n"
        "}\n\n"
        "type SdlError {\n"
        "  code: String!\n"
        "  message: String!\n"
        "  context_json: String!\n"
        "}\n\n"
        "type SdlConfigureResult {\n"
        "  attributes: SdlAttributeSetup!\n"
        "  records: SdlPersistSummary!\n"
        "  templates: SdlPersistSummary!\n"
        "}\n\n"
        "type SdlAttributeSetup {\n"
        "  created: Int!\n"
        "  existing: Int!\n"
        "  failed: [SdlSetupFailure!]!\n"
        "}\n\n"
        "type SdlPersistSummary {\n"
        "  supported: Boolean!\n"
        "  reason: String\n"
        "  count: Int!\n"
        "  persisted: Int\n"
        "  failed: [SdlSetupFailure!]!\n"
        "}\n\n"
        "type SdlSetupFailure {\n"
        "  attribute: String\n"
        "  record: String\n"
        "  template: String\n"
        "  datatype: String\n"
        "  reason: String!\n"
        "}\n"
    >>.
